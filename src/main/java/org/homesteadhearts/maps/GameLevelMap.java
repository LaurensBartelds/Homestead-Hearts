    private int[][] mergeLayersWithPriority(int[][] groundLayer, int[][] topLayer) {
        int[][] result = new int[groundLayer.length][groundLayer[0].length];

        for (int row = 0; row < groundLayer.length; row++) {
            for (int col = 0; col < groundLayer[row].length; col++) {
                if (row < topLayer.length && col < topLayer[row].length && topLayer[row][col] != 0) {
                    result[row][col] = topLayer[row][col];
                } else {
                    result[row][col] = groundLayer[row][col];
                }
            }
        }
        return result;
    }

